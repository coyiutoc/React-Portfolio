export default function getIcon(key) {
    if (key === "INVISION") {
      return "fab fa-invision";
    }
    else if (key === "WEBSITE") {
      return "fas fa-link";
    }
    else if (key === "GITHUB") {
      return "fab fa-github"
    }
    else if (key === "IMAGE") {
      return "fas fa-file-image";
    }
    else if (key === "VIDEO") {
      return "fas fa-file-video";
    }
}
